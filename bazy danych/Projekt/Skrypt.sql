IF OBJECT_ID('dbo.Slots','U') IS NULL
CREATE TABLE dbo.Slots (
	Name VARCHAR(20) PRIMARY KEY NOT NULL)
GO

IF OBJECT_ID('dbo.Factions','U') IS NULL
CREATE TABLE dbo.Factions (
	Name VARCHAR(20) PRIMARY KEY NOT NULL)
GO

IF OBJECT_ID('dbo.EnemyType','U') IS NULL
CREATE TABLE dbo.EnemyType (
	--ID INT PRIMARY KEY IDENTITY(1,1),
	Name VARCHAR(20) PRIMARY KEY NOT NULL)
GO

IF OBJECT_ID('dbo.Classes','U') IS NULL
CREATE TABLE dbo.Classes (
	Name VARCHAR(20) PRIMARY KEY NOT NULL)
GO

IF OBJECT_ID('dbo.BaseItems','U') IS NULL
CREATE TABLE dbo.BaseItems (
	ID INT NOT NULL,
	Name VARCHAR(50) NOT NULL,
	Slot VARCHAR(20) NOT NULL,
	PRIMARY KEY (ID),
	CONSTRAINT FK_Slot FOREIGN KEY (Slot) REFERENCES dbo.Slots(Name))
GO

IF OBJECT_ID('dbo.Attributes','U') IS NULL
CREATE TABLE dbo.Attributes (
	ID INT PRIMARY KEY NOT NULL,
	Type Varchar(20) NOT NULL,
	Name VARCHAR(20) NOT NULL,
	Base Float NOT NULL,
	Descr VARCHAR(100) NOT NULL)
GO

IF OBJECT_ID('dbo.Monsters','U') IS NULL
CREATE TABLE dbo.Monsters (
	ID INT PRIMARY KEY NOT NULL,
	Name VARCHAR(50) UNIQUE NOT NULL,
	Faction VARCHAR(20) NOT NULL,
	Type VARCHAR(20) NOT NULL,
	CONSTRAINT FK_Faction FOREIGN KEY (Faction) REFERENCES dbo.Factions(Name),
	CONSTRAINT FK_Type FOREIGN KEY (Type) REFERENCES dbo.EnemyType(Name))
GO

IF OBJECT_ID('dbo.Prizes','U') IS NULL
CREATE TABLE dbo.Prizes (
	ID INT PRIMARY KEY NOT NULL,
	Prefix INT NOT NULL,
	Namee INT NOT NULL,
	Suffix INT NOT NULL,
	[Item Level] INT NOT NULL,
	Slot varchar(20) NOT NULL,
	Owner INT,
	CONSTRAINT FK_Prefix FOREIGN KEY (Prefix) REFERENCES dbo.Attributes(ID),
	CONSTRAINT FK_Suffix FOREIGN KEY (Suffix) REFERENCES dbo.Attributes(ID),
	CONSTRAINT FK_Name FOREIGN KEY (Namee) REFERENCES dbo.BaseItems(ID),
	CONSTRAINT FK_Slot_Pr FOREIGN KEY (Slot) REFERENCES dbo.Slots(Name))
GO

IF OBJECT_ID('dbo.Characters','U') IS NULL
CREATE TABLE dbo.Characters (
	ID INT PRIMARY KEY NOT NULL,
	Name VARCHAR(100) NOT NULL,
	Class VARCHAR(20) NOT NULL,
	Level INT DEFAULT 1 NOT NULL,
	WEAPON INT,
	OFFHAND INT,
	HEAD INT,
	SHOULDERS INT,
	CHEST INT,
	HANDS INT,
	FEET INT,
	LEGS INT
	CONSTRAINT FK_Class FOREIGN KEY (Class) REFERENCES dbo.Classes(Name),
	CONSTRAINT FK_WEAPON FOREIGN KEY (WEAPON) REFERENCES dbo.Prizes(ID),
	CONSTRAINT FK_OFFHAND FOREIGN KEY (OFFHAND) REFERENCES dbo.Prizes(ID),
	CONSTRAINT FK_HEAD FOREIGN KEY (HEAD) REFERENCES dbo.Prizes(ID),
	CONSTRAINT FK_SHOULDERS FOREIGN KEY (SHOULDERS) REFERENCES dbo.Prizes(ID),
	CONSTRAINT FK_CHEST FOREIGN KEY (CHEST) REFERENCES dbo.Prizes(ID),
	CONSTRAINT FK_HANDS FOREIGN KEY (HANDS) REFERENCES dbo.Prizes(ID),
	CONSTRAINT FK_FEET FOREIGN KEY (FEET) REFERENCES dbo.Prizes(ID),
	CONSTRAINT FK_LEGS FOREIGN KEY (LEGS) REFERENCES dbo.Prizes(ID))
GO

IF OBJECT_ID('dbo.Bounties','U') IS NULL
CREATE TABLE dbo.Bounties (
	ID INT PRIMARY KEY IDENTITY(1,1),
	Goal INT NOT NULL,
	Enemy VARCHAR(50) NOT NULL,
	Difficulty VARCHAR(20) NOT NULL,
	Prize INT NOT NULL,
	Assigned INT,
	CONSTRAINT FK_Enemy FOREIGN KEY (Enemy) REFERENCES dbo.Monsters(Name),
	CONSTRAINT FK_Difficulty FOREIGN KEY (Difficulty) REFERENCES dbo.EnemyType(Name),
	CONSTRAINT FK_Prize FOREIGN KEY (Prize) REFERENCES dbo.Prizes(ID),
	CONSTRAINT FK_Character FOREIGN KEY (Assigned) REFERENCES dbo.Characters(ID))
GO
