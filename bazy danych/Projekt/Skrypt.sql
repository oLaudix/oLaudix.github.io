CREATE PROCEDURE Kills @Character INT AS
	Select Faction, Sum(Goal) as [Total Kills] FROM (SELECT Assigned, Goal, Name, Faction, Type FROM Bounties B JOIN Monsters M ON B.Enemy = M.Name) P WHERE P.Assigned = @Character GROUP BY Faction --and Faction = 'Undead'--and Type = 'Common'
GO

IF OBJECT_ID('Attacks','P') IS NOT NULL
	DROP PROCEDURE Attacks
GO

CREATE PROCEDURE Attacks @Faction varchar(20) AS
	Select (Select Name FROM Characters WHERE ID = Assigned) as Character, Count(*) as Attacks FROM (SELECT Goal, Assigned, Name, Faction, Type FROM Bounties B JOIN Monsters M ON B.Enemy = M.Name) P WHERE P.Faction = @Faction GROUP BY Assigned --and Faction = 'Undead' and Type = 'Common'
GO

