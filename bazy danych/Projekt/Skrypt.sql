	DROP PROCEDURE Inventory
GO

CREATE PROCEDURE Inventory @CharacterID INT AS
	Select ((Select Name FROM Attributes WHERE ID = Prefix) + ' ' + (Select Name FROM BaseItems WHERE ID = Namee) + ' ' + (Select Name FROM Attributes WHERE ID = Suffix)) as Name, [Item Level], Slot From Prizes Where Owner = @CharacterID ORDER BY [Item Level] DESC
GO

IF OBJECT_ID('BestItem','P') IS NOT NULL
	DROP PROCEDURE BestItem
GO

CREATE PROCEDURE BestItem @CharacterID INT, @Slot as varchar(20) AS
	Select TOP 1 ((Select Name FROM Attributes WHERE ID = Prefix) + ' ' + (Select Name FROM BaseItems WHERE ID = Namee) + ' ' + (Select Name FROM Attributes WHERE ID = Suffix)) as Name, [Item Level] From Prizes WHERE OWNER = @CharacterID and Slot = @Slot ORDER BY [Item Level] DESC
GO

